#include "main.h"
#include "stm32l0xx_hal.h"
#include "lcd_drv_hd44780.h"

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
void Error_Handler(void);

int main(void){
    unsigned char i=0;

    HAL_Init();                         // STM HALドライバ(ハード用API)の初期化
    SystemClock_Config();               // システムクロックの初期設定
    lcd_init();                         // LCDドライバの初期化
    lcd_putstr("LoRaWAN LCD");          // "LoRaWAN --- LCD"を表示
    lcd_goto_line(2);                   // LCDの2行目へ移動
    lcd_putstr("by Wataru KUNINO");     // LCDへメッセージを表示
    lcd_goto_line(1);                   // LCDの1行目へ移動

    while (1){
        lcd_goto(12);                   // LCDの(12+1)文字目へ移動
        lcd_disp_3(i);                  // 変数iの3桁の整数を表示
        i++;                            // 変数iに1を加算
        HAL_Delay(92);                  // 92msの待ち時間処理
    }
}

